        printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__LOCALIZED_FILE__STATS}\n" "${__v_loop_compiled_file_path}"; echo;

        printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__LOCALIZED_FILE__BYTES}\n" "$(BU.DevBin.LibCompilerV4.Functions.BytesToHuman  "$(wc -c < "${__v_loop_compiled_file_path}")" 'NULL'    1000 "${____sys_lang}")";
        printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__LOCALIZED_FILE__CHARS}\n" "$(BU.Main.BasicMaths.SeparateNumberEveryXDigits   "$(wc -m < "${__v_loop_compiled_file_path}")" ' '       3)";
        printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__LOCALIZED_FILE__LINES}\n" "$(BU.Main.BasicMaths.SeparateNumberEveryXDigits   "$(wc -l < "${__v_loop_compiled_file_path}")" ' '       3)";
        printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__LOCALIZED_FILE__WIDTH}\n" "$(BU.Main.BasicMaths.SeparateNumberEveryXDigits   "$(wc -L < "${__v_loop_compiled_file_path}")" ' '       3)";
        printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__LOCALIZED_FILE__WORDS}\n" "$(BU.Main.BasicMaths.SeparateNumberEveryXDigits   "$(wc -w < "${__v_loop_compiled_file_path}")" ' '       3)";

        # Exiting the script's loop after the compilation of the multi-language file.
        if [ -n "${__vMandatoryArgLangInclude}" ] && [ -z "${__vArrayVal_compile_stable}" ]; then break; fi

        # Adding the newly compiled file in the compiled files list.
        [ -n "${__vMandatoryArgLang}" ] && __BU_ARRAY__COMPILED_FILES_LIST+=("${__v_loop_compiled_file_path}");

        if [ -n "${__vArrayVal_compile_stable}" ]; then

            # Checking if the "stable" directory exists.
            if [ ! -d "${__v_loop_compiled_stable_file_parent_dir}" ]; then
                mkdir -p "${__v_loop_compiled_stable_file_parent_dir}" || {
                    echo "${__ERROR}FAILED TO CREATE THE ${__HIGHLIGHT}${__v_loop_compiled_stable_file_parent_dir}${__ERROR} DIRECTORY${__RESET}" >&2;
                    echo "${__ERROR}Please check the ${__RESET}" >&2;
                    echo >&2;

                    exit 1;
                };
            fi

            # Assigning values to the "${__v_loop_compiled_stable_file_path}" variables.
            if [ -n "${__vMandatoryArgLang}" ]; then
                __v_loop_compiled_stable_file_path="${__v_loop_compiled_stable_file_parent_dir}/Bash-utils-stable-${__v_loop_curr_locale}.sh";

            elif [ -n "${__vMandatoryArgLangInclude}" ]; then
                # If every languages codes stored into the "${___BU_COMPILER__LANG_ARRAY}" array are passed, then the compiled file will have the "full" string in its name.
                if diff <(printf "%s\n" "${__language_array[@]}" | sort) <(printf "%s\n" "${___BU_COMPILER__LANG_ARRAY[@]}" | sort) >/dev/null; then
                    __v_loop_compiled_stable_file_path="${__v_loop_compiled_stable_file_parent_dir}/Bash-utils-stable-full.sh";

                # Else, if not every languages codes stored into the "${___BU_COMPILER__LANG_ARRAY}" array are passed, then the compiled file will have the "multilang" string in its name.
                else
                    __v_loop_compiled_stable_file_path="${__v_loop_compiled_stable_file_parent_dir}/Bash-utils-multilang.sh";
                fi
            fi

            #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
            #~ STABLE FILE COMPILATION ONLY : Checking for any programming error in the compiled file
            #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

            BU.DevBin.LibCompilerV4.Functions.PrintNewstepLine \
                "$(printf \
                    "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_N__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHECKING_ERRORS}" \
                    "${__v_loop_compiled_file_path}")" \
                'UPPER';

            # Since the compiled file must be as bugless as possible, it is mandatory to check this file for any programming error with the "$(shellcheck)" command.
            if ! BU.Main.DevTools.ShellcheckVerif "${__v_loop_compiled_file_path}" "${__vArrayVal_compile_stable}"; then
                BU.DevBin.LibCompilerV4.Functions.PrintErrorLine \
                    "$(printf \
                        "${__v_loop_locale_print_code__error} ${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHECKING_ERRORS__ERROR}" \
                        "${__v_loop_compiled_file_path}")" \
                    'FULL';

                ____loop_error='error'; break;
            fi

            BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine \
                "$(printf \
                    "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHECKING_ERRORS__SUCCESS}\n\n" \
                    "${__v_loop_compiled_file_path}")" \
                'LOWER';

            #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
            #~ STABLE FILE COMPILATION ONLY : Copying the compiled file into the "stable" directory
            #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

            BU.DevBin.LibCompilerV4.Functions.PrintNewstepLine \
                "$(printf \
                    "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_N__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__COPYING_FILE}" \
                    "${__v_loop_compiled_file_path}" \
                    "${__v_loop_compiled_stable_file_parent_dir}")" \
                'UPPER';

            if ! cp --verbose "${__v_loop_compiled_file_path}" "${__v_loop_compiled_stable_file_path}" ; then
                BU.DevBin.LibCompilerV4.Functions.PrintErrorLine \
                    "$(printf \
                        "${__v_loop_locale_print_code__error} ${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__COPYING_FILE__ERROR}" \
                        "${__v_loop_compiled_file_path}" \
                        "${__v_loop_compiled_stable_file_parent_dir}")" \
                    'FULL';

                echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__COPYING_FILE__ERROR_ADVICE_1}" >&2;
                echo >&2;

                echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__COPYING_FILE__ERROR_ADVICE_2}" >&2;
                echo >&2;

                BU.DevBin.LibCompilerV4.Functions.QuitErrorMessage_NumberOfFilesCompiled;

                ____loop_error='error';

                break;
            else
                BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine \
                    "$(printf \
                        "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__COPYING_FILE__SUCCESS}" \
                        "${__v_loop_compiled_file_path}" \
                        "${__v_loop_compiled_stable_file_parent_dir}")" \
                    'LOWER';
            fi

            #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
            #~ STABLE FILE COMPILATION ONLY : Setting the compiled file into read-only mode with the "$(chmod)" command
            #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

            BU.DevBin.LibCompilerV4.Functions.PrintNewstepLine \
                "$(printf \
                    "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_N__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD}" \
                    "${__v_loop_compiled_file_path}")" \
                'UPPER';

            if ! chmod --verbose -xw "${__v_loop_compiled_stable_file_path}"; then
                BU.DevBin.LibCompilerV4.Functions.PrintWarningLine "$(printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING}" "${__v_loop_compiled_stable_file_path}")";

                printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__ASK}\n\n" "${__v_loop_compiled_stable_file_path}" >&2;
                read -rp "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__COPYING_FILE__ERROR_ENTER_YOUR_ANSWER}" __answer_copy_compiled_file_in_stable_dir;
                echo;

                case "${__answer_copy_compiled_file_in_stable_dir,,}" in
                    '1') echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_M__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__ANS_1}";;
                    '2' | *)
                        echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__ASK_2}" >&2; echo >&2;

                        # If other files were successfully compiled, but failed to be "chmoded".
                        if [ "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" -gt 0 ]; then
                            if [ "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" -eq 1 ]; then
                                echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__END_OF_COMPILATION__FILE_WHOSE_RIGHTS_HAVE_NOT_BEEN_MODIFIED}" >&2;

                                for files in "${__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}"; do
                                    echo "    - ${files}";
                                done
                            else
                                BU.DevBin.LibCompilerV4.Functions.PrintFilesWhichWereNotChmoded "${__v_loop_compiled_stable_file_parent_dir}";
                            fi
                        fi

                        BU.DevBin.LibCompilerV4.Functions.QuitErrorMessage_NumberOfFilesCompiled;

                        ____loop_error='error';

                        break;;
                esac

                # Adding the failed file into the array of failed files.
                [ -n "${__vMandatoryArgLang}" ] && __BU_ARRAY__READ_ONLY_FAILED_FILES+=("${__v_loop_compiled_stable_file_path}");

                BU.DevBin.LibCompilerV4.Functions.PrintWarningLine "$(printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COMPILED_STABLE_FILE_WAS_NOT_SET_IN_READ_ONLY_MODE}" "${__v_loop_compiled_stable_file_path}")" 'LOWER';
            else
                BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine "$(printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__COMPILED_STABLE_FILE_WAS_SUCCESSFULLY_SET_IN_READ_ONLY_MODE}" "${__v_loop_compiled_stable_file_path}")" 'LOWER';
            fi

            BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine "$(printf "${__v_loop_locale_print_code__success} ${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__STABLE_COMPILED_FILE_IS_READY_TO_BE_USED}" "${__v_loop_compiled_stable_file_path}")" 'FULL';

            # Adding the newly compiled stable file in the compiled stable files list.
            [ -n "${__vMandatoryArgLang}" ] && __BU_ARRAY__COMPILED_STABLE_FILES_LIST+=("${__v_loop_compiled_stable_file_path}");
        fi

        # If the '--lang-include=' value was passed as mandatory argument (create a single compiled file
        # with multiple languages shipped inside), then the execution of this script successfully stops.
        if [ -n "${__vMandatoryArgLangInclude}" ]; then break; fi

        # Increasing the iterator's value each time an individual language file is successfully compiled.
        __BU__BIN__LIB_COMPILER_V4__NUMBERS__COMPILER_LOOP_ITERATOR="$(( __BU__BIN__LIB_COMPILER_V4__NUMBERS__COMPILER_LOOP_ITERATOR + 1 ))";
    done;

    # If an error occured in the above loop, the execution of the compiler terminates.
    if [ -n "${____loop_error}" ] && [ "${____loop_error}" = 'error' ]; then
        if [ -n "${__vMandatoryArgLang}" ]; then
            BU.DevBin.LibCompilerV4.Functions.PrintErrorLine \
                "$(printf \
                    "${__v_loop_locale_print_code__error} ${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__CUSTOM_LANGUAGE_COMPILATION_FAILED}" \
                    "${__v_loop_curr_locale} ($(BU.Main.Locale.PrintLanguageName "${__v_loop_curr_locale}" 'cod,eng,usr,ori' 'no' 'false' 'true' 'true'))")" \
                'FULL';

            return 1;

        elif [ -n "${__vMandatoryArgLangInclude}" ]; then
            BU.DevBin.LibCompilerV4.Functions.PrintErrorLine \
                "$(printf \
                    "${__v_loop_locale_print_code__error} ${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__MULTI_LANGUAGES_COMPILATION_FAILED}" \
                    "${__v_loop_curr_locale} ($(BU.Main.Locale.PrintLanguageName "${__v_loop_curr_locale}" 'cod,eng,usr,ori' 'no' 'false' 'true' 'true'))")" \
                'FULL';

            return 1;
        fi
    fi

    # If the framework was compiled in a stable version.
    if [ -n "${__vArrayVal_compile_stable}" ]; then

        # If one or more stable files were not successsfully "chmoded".
        if [ "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" -gt 0 ]; then

            # If only one file was not "chmoded".
            if [ "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" -eq 1 ]; then
                BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine \
                    "$(printf \
                        "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__ONE_FILE_NOT_CHMODED}" \
                        "${__v_loop_compiled_stable_file_path}")";

                echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__END_OF_COMPILATION__FILE_WHOSE_RIGHTS_HAVE_NOT_BEEN_MODIFIED}";

                for files in "${__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}"; do
                    echo "    - ${files}";
                done

                echo >&2; printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__END_OF_COMPILATION__FILE_WHOSE_RIGHTS_HAVE_NOT_BEEN_MODIFIED__ADVICE}\n" "${__v_loop_compiled_stable_file_path}" >&2;

                # Else, if more than one file were not successfully "chmoded".
            else
                #**** Variables ****
                local v_index=0;
                local v_file_line;

                #**** Code ****
                if [ "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" -lt 5 ]; then
                    if [ "${#__BU_ARRAY__COMPILED_STABLE_FILES_LIST[@]}" == "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" ]; then
                        BU.DevBin.LibCompilerV4.Functions.PrintWarningLine \
                            "$(printf \
                                "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__EVERY_FILES_NOT_CHMODED}" \
                                "${#__BU_ARRAY__COMPILED_STABLE_FILES_LIST[@]}")";
                    else
                        BU.DevBin.LibCompilerV4.Functions.PrintWarningLine \
                            "$(printf \
                                "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__MULTIPLE_FILES_NOT_CHMODED}" \
                                "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" \
                                "${#__BU_ARRAY__COMPILED_STABLE_FILES_LIST[@]}")";
                    fi
                else
                    if [ "${#__BU_ARRAY__COMPILED_STABLE_FILES_LIST[@]}" == "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" ]; then
                        BU.DevBin.LibCompilerV4.Functions.PrintWarningLine \
                            "$(printf \
                                "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__EVERY_FILES_NOT_CHMODED}" \
                                "${#__BU_ARRAY__COMPILED_STABLE_FILES_LIST[@]}")" \
                            'FULL';
                    else
                        BU.DevBin.LibCompilerV4.Functions.PrintWarningLine \
                            "$(printf \
                                "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__WARNING__MULTIPLE_FILES_NOT_CHMODED}" \
                                "${#__BU_ARRAY__READ_ONLY_FAILED_FILES[@]}" \
                                "${#__BU_ARRAY__COMPILED_STABLE_FILES_LIST[@]}")" \
                            'FULL';
                    fi
                fi

                BU.DevBin.LibCompilerV4.Functions.PrintFilesWhichWereNotChmoded "${__v_loop_compiled_stable_file_parent_dir}";
            fi
        fi

        if [ "${#__BU_ARRAY__COMPILED_FILES_LIST}" -ge 1 ]; then
            if      [ "${#__BU_ARRAY__COMPILED_FILES_LIST}" -eq 1 ]; then
                    BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine \
                        "$(printf \
                            "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__SUCCESS_EQ_1}" \
                            "${#__BU_ARRAY__COMPILED_FILES_LIST[@]}")";

            elif    [ "${#__BU_ARRAY__COMPILED_FILES_LIST}" -gt 1 ]; then
                    BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine \
                        "$(printf \
                            "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__COPY_COMPILED_FILE_IN_STABLE_DIRECTORY__CHMOD__SUCCESS_GT_1}" \
                            "${#__BU_ARRAY__COMPILED_FILES_LIST[@]}")";
            fi
        fi
    fi

    BU.DevBin.LibCompilerV4.Functions.PrintSuccessLine "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_S__END_OF_FRAMEWORK_COMPILATION}" 'FULL';

    if [[ "${0##*/}" != lib-compiler-for-all-supported-versions.?(ba)sh ]]; then return 0; fi
}

