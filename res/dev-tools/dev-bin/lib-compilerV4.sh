# If the compiler isn't executed from the "lib-compiler-for-all-supported-versions.sh" file, then
# the "BU.DevBin.LibCompilerV4.CompileInSingleFile()" function is directly called, and the script
# will be exited with the "$(exit)" command, which is not something wanted for the execution of
# this script from the aforementioned file.
if [[ "${0##*/}" != lib-compiler-for-all-supported-versions.?(ba)sh ]]; then

    # Support of the arguments when this script is executed with the mandatory argument and the optional ones.
    if [ -n "${__BU__BIN__LIB_COMPILER_V4__ARGS__LANG_LIST_STR_TO_ARRAY}" ]; then 
        BU.DevBin.LibCompilerV4.CompileInSingleFile "${__BU__BIN__LIB_COMPILER_V4__ARGS__LANG_LIST_STR_TO_ARRAY}" "${@}" || { exit 1; };

    # If no options are passed, instead of throwing an error, the "BU.DevBin.LibCompilerV4.CompileInSingleFile()" function will be called with the supported languages argument.
    else
        BU.DevBin.LibCompilerV4.CompileInSingleFile "lang=en,fr" "${@}" || { exit 1; };
    fi

    # If the compiler was not executed from the "lib-compiler-for-all-supported-versions.sh" script, then this script can be exited.
    exit 0;
fi
