#### COMPILATION

## COMPILER'S MAIN FUNCTION

# ······························
# Main function of the compiler.

# shellcheck disable=SC2059
function BU.DevBin.LibCompilerV4.CompileInSingleFile()
{
    #**** Parameters ****
    local p_locale=${1:-NULL};  # ARG TYPE : String     - REQUIRED | DEFAULT VAL : NULL     - DESC : Language in which the file must be translated.

    # This variable will be used to verify if the "lang=" value is passed as first argument.
    local v_is_check_to_do;     # VAR TYPE : Bool       - DESC :

    # Getting the current system language.
    local ____sys_lang;         # VAR TYPE : CMD        - DESC : This variable stores a ISO 639-1 code, copied from the two first characters of the operating system's ICU locale.

    local __language_array;     # VAR TYPE : Array      - DESC : Array of ISO 639-1 codes.

    #**** Code ****

    # Parsing the array of optional arguments given by the user.
    BU.DevBin.LibCompilerV4.PreCompilationFunctions.OptionalArgumentsArrayLooping;

    # Checking if any incompatible optional values were passed together during the execution of the compiler.
    BU.DevBin.LibCompilerV4.PreCompilationFunctions.CheckForAnyIncompatibleOptionalArgs;

    # Processing the authorization to skip the verification of a file before its compilation.
    BU.DevBin.LibCompilerV4.PreCompilationFunctions.ManageShellcheck;

    # Managing the ICU locale of the user's operating system.
    ____sys_lang="$(echo "${LANG}" | cut -d _ -f1)";

    # If the '-h' or the '--help' values were passed as the very first parameter.
    if [[ "${p_locale,,}" == ?(-?(-))h?(elp) ]]; then
        BU.DevBin.LibCompilerV4.Functions.CompilerUsage;

    # Converting the "${p_locale}" string into an array of ISO 639-1 codes.
    elif [[ "${p_locale,,}" == ?(-?(-))lang?(-include)=* ]]; then

        #**** Conditional variables ****
        local key;                  # VAR TYPE : Char       - DESC : User's keyboard input to make the compiler choosing a compilation option in case it cannot determine what the user chose (safeguard mesure, in case the "${p_locale}" or the "${p_locale_old}" values are modified).

        local v_locale_str;         # VAR TYPE : String     - DESC : This variable stores the mandatory first argument's value if it contains the "--lang=" sub-string.
        local v_locale_tmp;         # VAR TYPE : String     - DESC : This variable temporarily stores the above variable's string.
        local v_locale_delim;       # VAR TYPE : Char       - DESC :

        local v_p_locale_old;       # VAR TYPE : String     - DESC :

        #**** Conditional code ****

        # Checking for the delimiter.
        if      BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" ','; then v_locale_delim=',';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" ';'; then v_locale_delim=';';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" '|'; then v_locale_delim='|';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" '~'; then v_locale_delim='~';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" '_'; then v_locale_delim='_';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" '.'; then v_locale_delim='.';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" ':'; then v_locale_delim=':';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" '!'; then v_locale_delim='!';
        elif    BU.DevBin.LibCompilerV4.Functions.CheckLangArgDelim "${p_locale}" '§'; then v_locale_delim='§';
        fi

        # Getting each ISO 639-1 codes after the mandatory '=' sign.
        v_locale_str="$(sed "s/^[^=]*=//" <<< "${p_locale}")";

        # This variable's purpose is to avoid assigning the "${v_locale_str}" variable to itself, because if so, the assignment would have done nothing.
        v_locale_tmp="${v_locale_str}";
        v_locale_str="${v_locale_tmp}";

        # If the "${v_locale_str}" variables does not contains, the "all/-supported" value, then   .
        if [ "${v_locale_str,,}" != 'all' ] && [[ "${v_locale_str,,}" != ?(all?(-))supported ]]; then
            # Replacing each delimiter with an empty character.
            v_locale_str="${v_locale_str//${v_locale_delim}/" "}";

            # String to word array.
            read -ra __language_array <<< "${v_locale_str}";
        else
            # Creating a variable which stores the old value of the "${p_locale}" variable, so that the "${__vMandatoryArgLang}" or the "${__vMandatoryArgLangInclude}" variables can be created.
            v_p_locale_old="${p_locale}";

            # Reassigning the new value in the "${v_locale_str}" variable into the "${p_locale}" original variable, so that the condition can be written only once.
            p_locale="${v_locale_str}";

            # Assigning a value, so that no extra checkings are done to the first argument's value afterwards.
            v_is_check_to_do='true';
        fi

        # Checking if the "--lang=" value was passed, so that a file will be compiled for each language.
        if [[ "${p_locale,,}" == ?(-?(-))lang=* ]] || [[ "${v_p_locale_old,,}" == ?(-?(-))lang=* ]]; then
            __vMandatoryArgLang='--lang=';

        # Checking if the "--lang-include=" value was passed, so that each language will be embedded in the compiled file.
        elif [[ "${p_locale,,}" == ?(-?(-))lang-include=* ]] || [[ "${v_p_locale_old,,}" == ?(-?(-))lang-include=* ]]; then
            __vMandatoryArgLangInclude='--lang-include';

        else
            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COMPILER_UNABLE_TO_DETERMINE_FIRST_ARG__MSG_1}" >&2;
            echo >&2;

            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COMPILER_UNABLE_TO_DETERMINE_FIRST_ARG__MSG_2}" >&2;
            echo >&2;

            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__COMPILER_UNABLE_TO_DETERMINE_FIRST_ARG__USER_INPUT}" >&2;
            echo >&2;

            read -r -n 1 key;

            if [[ "${key^^}" != "Q" && "${key}" != $'\e' ]]; then
                if [ "${key^^}" != 'L' ]; then __vMandatoryArgLangInclude='--lang-include'; else __vMandatoryArgLang='--lang'; fi
            else
                exit 1;
            fi
        fi
    else
        # Assigning a value, so that no extra checkings are done to the first argument's value afterwards.
        v_is_check_to_do='true';
    fi

    # Else, if the "all" value is passed as the first argument.
    if [ "${v_is_check_to_do}" == 'true' ]; then
        if [ "${p_locale,,}" == 'all' ]; then
            BU.DevBin.LibCompilerV4.Functions.PrintWarningLine "${__HIGHLIGHT}${p_locale}${__WARNING} | ${__HIGHLIGHT}lang=all${__WARNING} : THIS FEATURE IS NOT YET FULLY IMPLEMENTED, WATCH OUT FOR THE BUGS" 'FULL';

            return 1;

            # for langs in "${___BU_COMPILER__LANG_ARRAY[@]}"; do __language_array+=("${langs}"); done;

        # Else, if the "all-supported" or "supported" value is passed as the first argument.
        elif [[ "${p_locale,,}" == ?(all?(-))supported ]]; then
            for langs in "${__BU_COMPILER__SUPPORTED_LANG_ARRAY[@]}"; do __language_array+=("${langs}"); done;

        else
            BU.DevBin.LibCompilerV4.Functions.PrintErrorLine "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__BAD_LANG_ARRAY_PASSED__ERROR_MSG}" 'FULL';
            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__BAD_LANG_ARRAY_PASSED__ADVICE_1}" >&2;
            echo >&2;

            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__BAD_LANG_ARRAY_PASSED__ADVICE_2}" >&2;
            echo "'lang=en fr uk sv tk kl'" >&2;
            echo >&2;

            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__BAD_LANG_ARRAY_PASSED__ADVICE_3} ${__HIGHLIGHT},;|~_.:!§${__RESET}" >&2;
            echo >&2;

            echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__BAD_LANG_ARRAY_PASSED__ADVICE_4}" >&2;
            echo >&2; echo >&2;

            printf "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__BAD_LANG_ARRAY_PASSED__HELP}\n" '-h' '--help' >&2;

            # Uncomment the following line if you need to check the arguments list in case of a problem with them while executing the compiler.
            BU.DevBin.LibCompilerV4.Functions.Debug;

            BU.DevBin.LibCompilerV4.Functions.PrintErrorLine "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__PRINT_NO_FILES_WERE_COMPILED_ERROR_MSG}" 'FULL';

            return 1;
        fi

        unset v_is_check_done;
    fi

    ## ==============================================

    ## COMPILATION PROCESS

    for language in "${__language_array[@]}"; do
        if [ "${__BU__BIN__LIB_COMPILER_V4__NUMBERS__COMPILER_LOOP_ITERATOR}" -eq 0 ]; then
            #------------------------
            #**** Loop variables ****
            local __v_loop_curr_locale;                # VAR TYPE : ISO 639-1 code     - DESC : ISO 639-1 code of the language to process into this loop.

            local __v_loop_locale_file_path;           # VAR TYPE : Filepath   - DESC : Path to the current locale's translation file of the framework's initialization script.
            local __v_loop_locale_file_path_en;        # VAR TYPE : Filepath   - DESC : Path to the English translation file of the framework's initialization script.

            # Compiled file's informations.
            local __v_loop_compiled_file_parent_dir;   # VAR TYPE : Dirpath    - DESC : Path to the
            local __v_loop_compiled_file_path;         # VAR TYPE : Filepath   - DESC : Path to the

            local __v_loop_locale_print_code;          # VAR TYPE : String     - DESC : Printing the current language's name.

            local __v_loop_locale_print_code__error;   # VAR TYPE : String     - DESC : Printing the current language's name and coloring the concatened text with the error messages color code.
            local __v_loop_locale_print_code__newstep; # VAR TYPE : String     - DESC : Printing the current language's name and coloring the concatened text with the newstep messages color code.
            local __v_loop_locale_print_code__success; # VAR TYPE : String     - DESC : Printing the current language's name and coloring the concatened text with the success messages color code.
            local __v_loop_locale_print_code__warning; # VAR TYPE : String     - DESC : Printing the current language's name and coloring the concatened text with the warning messages color code.

            # If the "${_____value_of__compile_stable}" value was passed in the array of optional arguments.
            if [ -n "${__vArrayVal_compile_stable}" ]; then
                local __v_loop_compiled_stable_file_parent_dir;    # VAR TYPE : Dirpath    - DESC :
                local __v_loop_compiled_stable_file_path;          # VAR TYPE : Filepath   - DESC :

                __v_loop_compiled_stable_file_parent_dir="${__BU__BIN__LIB_COMPILER_V4__RESOURCES__COMPILED_DIR_PATH}/stable";
            fi

            # If the "${_____value_of__no_shellcheck}" value was passed in the array of optional arguments.
            if [ -n "${__vArrayVal_no_shellcheck}" ]; then local __no_shellcheck="${__vArrayVal_no_shellcheck}"; fi

            #----------------------------
            #**** Loop sub-functions ****

            function BU.DevBin.LibCompilerV4.CompileInSingleFile.Loop.IsCurrentLanguageEnglish()
            {
                if [[ ( -z "${__vArrayVal_no_english_include}" ) && ! "${__language_array[*]}" =~ en ]] || [ "${__v_loop_curr_locale,,}" != 'en' ]; then
                    return 1;
                else
                    return 0;
                fi
            }
        fi

        #-------------------
        #**** Loop code ****
        __v_loop_curr_locale="${language}";
        __v_loop_locale_file_path="${__BU_MODULE_INIT_TRANSLATIONS_PATH}/${__v_loop_curr_locale}.locale";
        __v_loop_locale_file_path_en="${__BU_MODULE_INIT_TRANSLATIONS_PATH}/en.locale";

        __v_loop_compiled_file_parent_dir="${__BU__BIN__LIB_COMPILER_V4__RESOURCES__COMPILED_DIR_PATH}/unstable";

        # Path to the temporary output file (global variable).
        __BU__BIN__LIB_COMPILER_V4__RESOURCES__FULL_FILE_PATH="${__v_loop_compiled_file_parent_dir}/Bash-utils-compilation-output.tmp";

        # TODO : separate the declaration of the variables which have to be redeclared for each loop iteration.
        # readonly __BU__BIN__LIB_COMPILER_V4__RESOURCES__FULL_FILE_PATH;

        if [ -n "${__vMandatoryArgLang}" ]; then
            __compiled_file_name="Bash-utils-${__v_loop_curr_locale}.sh";

        elif [ -n "${__vMandatoryArgLangInclude}" ]; then
            # If every languages codes stored into the "${___BU_COMPILER__LANG_ARRAY}" array are passed, then the compiled file will have the "full" string in its name.
            if diff <(printf "%s\n" "${__language_array[@]}" | sort) <(printf "%s\n" "${___BU_COMPILER__LANG_ARRAY[@]}" | sort) >/dev/null; then
                __compiled_file_name="Bash-utils-full.sh";

            # Else, if not every languages codes stored into the "${___BU_COMPILER__LANG_ARRAY}" array are passed, then the compiled file will have the "multilang" string in its name.
            else
                __compiled_file_name="Bash-utils-multilang.sh";
            fi
        fi

        __v_loop_compiled_file_path="${__v_loop_compiled_file_parent_dir}/${__compiled_file_name}";

        if [ "${#__language_array[@]}" -eq 1 ]; then
            __v_loop_locale_print_code="${__HIGHLIGHT}[ LOCALE : ${__v_loop_curr_locale} [$(BU.Main.Locale.PrintLanguageName "${__v_loop_curr_locale^^}" 'cod,eng,usr,ori' 'no' 'false' 'false' 'true')] ]";

        elif [ "${#__language_array[@]}" -gt 1 ]; then
            if [ -n "${__vMandatoryArgLang}" ]; then 
                __v_loop_locale_print_code="${__HIGHLIGHT}[ LOCALE : MULTILANG MULTI FILES | CURR -> [$(BU.Main.Locale.PrintLanguageName "${__v_loop_curr_locale^^}" 'cod,eng,usr,ori' 'no' 'false' 'false' 'true')] ]";
            else
                __v_loop_locale_print_code="${__HIGHLIGHT}[ LOCALE : MULTILANG SINGLE FILE | CURR -> [$(BU.Main.Locale.PrintLanguageName "${__v_loop_curr_locale^^}" 'cod,eng,usr,ori' 'no' 'false' 'false' 'true')] ]";
            fi
        fi

        __v_loop_locale_print_code__error="${__v_loop_locale_print_code}${__ERROR}";
        __v_loop_locale_print_code__newstep="${__v_loop_locale_print_code}${__NEWSTEP}";
        __v_loop_locale_print_code__success="${__v_loop_locale_print_code}${__SUCCESS}";
        __v_loop_locale_print_code__warning="${__v_loop_locale_print_code}${__WARNING}";

        # Checking if the "unstable" directory exists.
        if [ ! -d "${__v_loop_compiled_file_parent_dir}" ]; then
            mkdir -p "${__v_loop_compiled_file_parent_dir}" || {
                echo "${__ERROR}FAILED TO CREATE THE ${__HIGHLIGHT}${__v_loop_compiled_file_parent_dir}${__ERROR} DIRECTORY${__RESET}" >&2;
                echo "${__ERROR}Please check the ${__RESET}" >&2;
                echo >&2;

                BU.DevBin.LibCompilerV4.Functions.PrintErrorLine "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__PRINT_NO_FILES_WERE_COMPILED_ERROR_MSG}" 'FULL';

                exit 1;
            };
        fi

        # Deleting the existing "${__BU__BIN__LIB_COMPILER_V4__RESOURCES__FULL_FILE_PATH}" file.
        if [ -f "${__BU__BIN__LIB_COMPILER_V4__RESOURCES__FULL_FILE_PATH}" ] && [ -s "${__BU__BIN__LIB_COMPILER_V4__RESOURCES__FULL_FILE_PATH}" ]; then true > "${__BU__BIN__LIB_COMPILER_V4__RESOURCES__FULL_FILE_PATH}"; fi

        #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
        #~ Checking if the "${__v_loop_curr_locale}" variable is a valid ISO 639-1 language code
        #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

        if ! BU.DevBin.LibCompilerV4.Functions.CheckISO639_1_LangCode "${__v_loop_curr_locale}"; then
            BU.DevBin.LibCompilerV4.Functions.PrintErrorLine "${__v_loop_locale_print_code__error} ${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__BAD_LANGUAGE_PASSED}" 'FULL';

            ____loop_error='error';

            break;
        fi

        #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
        #~ Framework compilation process startup message
        #~ ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

