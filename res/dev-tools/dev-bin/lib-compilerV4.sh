## CHECKING FOR ANY INCOMPATIBLE VALUES

# ·······································································································
# Checking if any incompatible optional values were passed together during the execution of the compiler.

# shellcheck disable=
function BU.DevBin.LibCompilerV4.PreCompilationFunctions.CheckForAnyIncompatibleOptionalArgs()
{
    # Verifying if the "${__vArrayVal_no_shellcheck}" and the "${__vArrayVal_compile_stable}" values were passed together.
    if [[ (-n "${__vArrayVal_compile_stable}") && (-n "${__vArrayVal_no_shellcheck}") ]]; then
    
        # shellcheck disable=SC2059
        BU.DevBin.LibCompilerV4.Functions.PrintWarningLine \
            "$(printf \
                "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__NO_SHELLCHECK_AND_COMPILE_STABLE_WERE_PASSED_TOGETHER}\n\n" \
                "${__vArrayVal_compile_stable}" \
                "${__vArrayVal_no_shellcheck}")" \
            'FULL' >&2;

        echo "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_W__NO_SHELLCHECK_AND_COMPILE_STABLE_WERE_PASSED_TOGETHER__ADVICE}" >&2;

        echo >&2; echo >&2;

        BU.DevBin.LibCompilerV4.Functions.CompilerUsage 'false';

        BU.DevBin.LibCompilerV4.Functions.PrintErrorLine "${__BU__BIN__LIB_COMPILER_V4__GLOBVARS__MSG_E__PRINT_NO_FILES_WERE_COMPILED_ERROR_MSG}" 'FULL';

        exit 1;
    fi

    # Verifying if another "--keep-*" value was passed with the '--keep-raw-document-layout' parameter.
    if [ -n "${__vArrayVal_keep_raw_document_layout}" ]; then
        if  [ -n "${__vArrayVal_keep_comments}" ] || \
            [ -n "${__vArrayVal_keep_exec_safeguards}" ] || \
            [ -n "${__vArrayVal_keep_functions_infos}" ] || \
            [ -n "${__vArrayVal_keep_functions_pvc_infos}" ] || \
            [ -n "${__vArrayVal_remove_shellcheck_directives}" ];
        then
            BU.DevBin.LibCompilerV4.Functions.HandleIncompatibleOptionalArgs \
                "${__vArrayVal_keep_comments}" \
                "${__vArrayVal_keep_exec_safeguards}" \
                "${__vArrayVal_keep_functions_infos}" \
                "${__vArrayVal_keep_functions_pvc_infos}" \
                "${__vArrayVal_remove_shellcheck_directives}";
        fi
    fi    
}

